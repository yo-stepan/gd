$.widget("block.feature_map", {

    _create: function() {
        console.log('go feature map');

        var block = this,
            $block = this.element;

        block.$mapShow = $block.find('.feature__mapShow');
        block.$map = $block.find('.map');

        block.$mapShow.on('click', function() {
            //$block.addClass('feature_open');
            $block.animate({
                height: 500
            }, 300, function() {
                block.$map.mapmap('expand');
            });

            return false;
        });

    }

});